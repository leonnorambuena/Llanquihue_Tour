    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        if (listaEntidades.isEmpty()) {
            txtArea1.setText("No hay registros guardados en el sistema.");
            return;
        }

        txtArea1.setText("");
        txtArea1.append("=== REGISTROS DE LLANQUIHUE TOUR ===\n\n");

        for (model.Registrable entidad : listaEntidades) {

            txtArea1.append(entidad.mostrarResumen() + "\n");

            if (entidad instanceof model.GuiaTuristico) {
                model.GuiaTuristico guia = (model.GuiaTuristico) entidad;
            } else if (entidad instanceof model.Vehiculo) {
                model.Vehiculo vehiculo = (model.Vehiculo) entidad;
            }

            txtArea1.append("--------------------------------------------------------------------------------\n");
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

