    private void btnGuiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuiaActionPerformed

        String nombre = jTextField4.getText().trim();
        String rut = jTextField5.getText().trim();

        if (nombre.isEmpty() || rut.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Por favor, complete todos los campos del guía (Nombre y RUT).",
                    "Campos vacíos",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        model.GuiaTuristico nuevoGuia = new model.GuiaTuristico(nombre, rut);

        listaEntidades.add(nuevoGuia);

        jTextField4.setText("");
        jTextField5.setText("");

        javax.swing.JOptionPane.showMessageDialog(this, "¡Guía registrado con éxito!");
    }//GEN-LAST:event_btnGuiaActionPerformed

