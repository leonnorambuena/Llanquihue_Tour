    private void btnAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAutoActionPerformed
        try {

            String patente = txtPatente.getText().trim();
            String capacidadStr = txtCapacidad.getText().trim();

            if (patente.isEmpty() || capacidadStr.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Por favor, complete los campos obligatorios (Patente y Capacidad).",
                        "Campos vacíos",
                        javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            int capacidad = Integer.parseInt(capacidadStr);

            model.Vehiculo nuevoVehiculo = new model.Vehiculo(patente, capacidad);
            listaEntidades.add(nuevoVehiculo);

            txtPatente.setText("");
            txtCapacidad.setText("");

            javax.swing.JOptionPane.showMessageDialog(this, "¡Vehículo registrado con éxito!");

        } catch (NumberFormatException e) {

            javax.swing.JOptionPane.showMessageDialog(this,
                    "La capacidad debe ser un número entero válido.",
                    "Error de formato",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAutoActionPerformed

